void	ft_rev_int_tab(int	*tab, int	size)
{
	int	a;
	int	s;
	int	d;

	a = 0;
	d = size - 1;
	while (a < size / 2)
	{
		s = tab[a];
		tab[a] = tab[d];
		tab[d] = s;
		a++;
		d--;
	}
}
