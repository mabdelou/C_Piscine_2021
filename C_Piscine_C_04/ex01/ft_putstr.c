#include <unistd.h>
void	print_str(char	a)
{
	write(1, &a, 1);
}

void	ft_putstr(char	*str)
{
	int	b;

	b = 0;
	while (str[b] != '\0')
	{
		print_str(str[b]);
		b++;
	}
}
