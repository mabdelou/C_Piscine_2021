int	ft_strcmp(char	*s1, char	*s2)
{
	int	b;

	b = 0;
	while (s1[b] != '\0' || s2[b] != '\0')
	{
		if (s1[b] == '\0' && s2[b] != '\0')
			return (-1);
		else if (s1[b] != '\0' && s2[b] == '\0')
			return (1);
		else if (s1[b] > s2[b])
			return (1);
		else if (s1[b] < s2[b])
			return (-1);
		b++;
	}
	return (0);
}
