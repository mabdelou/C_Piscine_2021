int	ft_strncmp(char	*s1,	char	*s2,	unsigned int	n)
{
	unsigned int	b;

	b = 0;
	while (s1[b] == s2[b] && s1[b] && s2[b] && b < n)
		b++;
	if (b != n)
		return (s1[b] - s2[b]);
	return (0);
}
